CODE SEGMENT
ASSUME CS:CODE,DS:CODE,ES:CODE,SS:CODE

PPIC_C EQU 1FH
PPIC EQU 1DH
PPIB EQU 1BH
PPIA EQU 19H

ORG 1000H
MOV AL,10000000B
OUT PPIC_C,AL

MOV SI,OFFSET FONT
L1:
MOV AL,BYTE PTR CS:[SI]
OUT PPIB,AL
CALL DELAY
JMP L1

MOV AL,11111111B
OUT PPIA,AL
CALL DELAY

JMP L1

DELAY:
MOV CX,1111111111111111B
TIMER1:
NOP
NOP
NOP
NOP
LOOP TIMER1
RET

FONT:
DB 00000001B

CODE ENDS
END
